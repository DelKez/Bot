module.exports.num = function (num){

    if (num == 0) {
        return "#ff3620"
    }
    else if (num == 1) {
        return "#ff5420"
    }
    else if (num == 2) {
        return "#ff8020"
    }
    else if (num == 4) {
        return "#ffa920"
    }
    else if (num == 8) {
        return "#ffe120"
    }
    else if (num == 16) {
        return "#84ff20"
    }
    else if (num == 32) {
        return "#20ff67"
    }
}

